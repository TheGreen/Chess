package de.janchristiangruenhage.util.terminal.menu;

/**
 * Created by jcgruenhage on 5/11/16.
 */
public class Item<T> implements MenuItem {
    public static Item NULL;
    private String name;
    private T content;

    public Item(String name, T content) {
        this.name = name;
        this.content = content;
    }

    public T getContent() {
        return content;
    }

    public void setContent(T content) {
        this.content = content;
    }

    @Override
    public MenuItem select() {
        return this;
    }

    @Override
    public String getName() {
        return name;
    }
}
